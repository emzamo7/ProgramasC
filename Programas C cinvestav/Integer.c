#include "Integer.h"

Integer Successor(Integer a){
	return a+1;
}
Integer Predecessor(Integer a){
	return a-1;
}
Integer Sum(Integer a, Integer b){
	
	return a+b;
}
Integer Diff(Integer a, Integer b){
	return a-b;
}
Integer Product(Integer a, Integer b){
	return a*b;
}
